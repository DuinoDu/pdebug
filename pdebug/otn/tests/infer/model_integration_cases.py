def _matrix_case(
    *,
    case_id: str,
    manifest: str,
    sample_data_types: tuple[str, ...],
    args: dict[str, object],
    expected_outputs: tuple[str, ...],
    requirements: tuple[str, ...],
    manual_sample_data: tuple[str, ...] = (),
    notes: tuple[str, ...] = (),
) -> dict[str, object]:
    """Rich case definition reviewed from infer signatures and tests."""
    runner_case = next(
        case for case in MODEL_INTEGRATION_CASES if case.case_id == case_id
    )
    return {
        "case_id": case_id,
        "file": runner_case.file,
        "node": runner_case.node,
        "manifest": manifest,
        "args_builder": runner_case.args_builder,
        "sample_data_types": sample_data_types,
        "args": args,
        "expected_outputs": expected_outputs,
        "requirements": requirements,
        "manual_sample_data": manual_sample_data,
        "notes": notes,
    }


