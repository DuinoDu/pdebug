def _by_file() -> Mapping[str, dict]:
    return {item["file"]: item for item in MODEL_INTEGRATION_MATRIX}


def _case(
    *,
    case_id: str,
    file: str,
    node: str,
    description: str,
    args_builder: str,
    requires_cuda: bool = False,
    requires_docker: bool = False,
    required_modules: tuple[str, ...] = (),
    expected_paths: tuple[str, ...] = (),
    repo_url: str | None = None,
    repo_downloads: tuple[tuple[str, str], ...] = (),
) -> IntegrationCase:
    metadata = _by_file()[file]
    return IntegrationCase(
        case_id=case_id,
        file=file,
        node=node,
        description=description,
        args_builder=args_builder,
        official_sources=tuple(metadata["official_sources"]),
        repo_url=repo_url
        if repo_url is not None
        else metadata["repo_url"][0]
        if metadata["repo_url"]
        else None,
        hf_model_ids=tuple(metadata["hf_model_ids"]),
        repo_downloads=repo_downloads,
        preferred_runtime=str(metadata["preferred_runtime"]),
        requires_cuda=requires_cuda,
        requires_docker=requires_docker,
        required_modules=required_modules,
        expected_paths=expected_paths,
        heavy_reason=str(metadata["heavy_reason"]),
    )


